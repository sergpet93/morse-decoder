const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    arrayExpr = expr.match(/.{10}/g);
    let result = '';
    for (let letter of arrayExpr){
        let code = '';
        if (letter === '**********') {
            result += ' ';
            continue
        }
        let arrayLet = letter.match(/.{2}/g);
        for (let i of arrayLet){
            if (i === '00') continue;
            code += i === '10' ? '.' : '-'
        }
        result += MORSE_TABLE[code];
    }
   return result
}

module.exports = {
    decode
}